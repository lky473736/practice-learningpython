# 실수형 (R) = floating point, fixed point 포함한 거 (소수점 포함한 거)
# E~ = 10^(~)
# ex) 1092E92 = 1092 * 10^(92)

b = 12.3
b = 12345E6789

# 8진수와 16진수 
# 8진수 = 0O 다음에 8진수 입력
# 16진수 = 0x 다음에 16진수 입력

c = 0O123 # 8진수 123
c = 0x123 # 16진수 123

# % = 나머지 출력 (modulo)
# // = 몫 출력 

print (123 % 10) # 3 출력
print (123 // 10) # 12 출력